#include <stdio.h>

int main(void)
{
    int max = -1; // Инициализируем максимум значением -1
    int number;

    // Читаем числа до тех пор, пока не встретим -1
    while (1) {
        if (scanf("%d", &number) != 1) {
            // Если ввод некорректен, завершаем программу
            return 0;
        }
        if (number == -1) {
            // Если встретили -1, завершаем ввод
            break;
        }
        if (number > max) {
            // Обновляем максимум, если текущее число больше
            max = number;
        }
    }

    // Выводим наибольшее число
    printf("%d", max);

    return 0;
}